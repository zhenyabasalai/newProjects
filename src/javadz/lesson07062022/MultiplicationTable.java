package javadz.lesson07062022;

public class MultiplicationTable {
    public static void main(String[] args) {

        for (int a = 1; a < 10; a++) {
            for (int b = 1; b < 10; b++) {
                System.out.print(b * a + "  ");
            }
            System.out.println("");

        }
    }

}
